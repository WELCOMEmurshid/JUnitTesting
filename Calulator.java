package agileUniteTest;

public class Calulator {
	
	public static int addMethod(int n1, int n2) {
		return (n1+n2);
	}
	public static int subMethod(int n1, int n2) {
		return (n1-n2);
	}
	public static int multMethod(int n1,int n2) {
		return (n1*n2);
	}
	public static double divMethod(double n1,double n2) {
		
		return (n1/n2);
	}

}
